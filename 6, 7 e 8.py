numero1 = float(input("Digite um numero"))
numero2 = float(input("Digite outro numero"))

if numero1 > numero2:
    print(f"O primeiro numero é maior que o segundo, pois {numero1} é maior que {numero2}")
elif numero1 < numero2:
    print(f"O primeiro numero é maior que o segundo, pois {numero2} é maior que {numero1}")
elif numero1 == numero2:
    print(f"os numeros {numero1} e {numero2}")
